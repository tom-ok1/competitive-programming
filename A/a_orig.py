from collections import deque
from typing import List


class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        visited = set()
        n = len(grid)
        m = len(grid[0])
        cnt = 0
        for i in range(n):
            for j in range(m):
                if (i, j) not in visited and grid[i][j] == "1":
                    q: deque[tuple[int, int]] = deque()
                    q.append((i, j))
                    while len(q):
                        i, j = q.popleft()
                        if (i, j) in visited or grid[i][j] == "0":
                            continue

                        visited.add((i, j))
                        if i < n - 1:
                            q.append((i + 1, j))
                        if i > 0:
                            q.append((i - 1, j))
                        if j < m - 1:
                            q.append((i, j + 1))
                        if j > 0:
                            q.append((i, j - 1))
                    cnt += 1

        return cnt


